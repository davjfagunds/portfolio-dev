# Exercício usando If e comparação

valor1 = input('Digite um Valor: ')
valor2 = input('Digite outro Valor: ')



if valor1 > valor2:
    print(f'{valor1} é maior do que {valor2}')
elif valor1 < valor2:
    print(f'{valor1} é menor do que {valor2}')
else:
    print(f'{valor1} e {valor2} são iguais')
